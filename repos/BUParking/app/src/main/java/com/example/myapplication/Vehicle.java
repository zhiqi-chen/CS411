package com.example.myapplication;

public class Vehicle {
    private String color;
    private String make;
    private String model;
    private String number;
    private String state;

    public Vehicle (String color, String make, String model, String number, String state) {
        this.color = color;
        this.make = make;
        this.model = model;
        this.number = number;
        this.state = state;
    }
}
